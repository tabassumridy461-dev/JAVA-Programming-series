public class VarKeyword {
    public static void main(String[] args) {

        var x = 5;           // int
        var name = "Java";   // String
        var price = 9.99;    // double

        System.out.println("x = " + x);
        System.out.println("name = " + name);
        System.out.println("price = " + price);
    }
}
